/**
 *  \brief pantalla_sort: Sort the elements in the array of employees, the argument order
 * indicate UP or DOWN order
 * \param Employee* list: Pointer to array of employees
 * \param int len: Array length
 * \param int order: [2] indicate UP (order Z->A) - [1] indicate DOWN (order A->Z)
 * \return (-1) Error / (0) Ok
 */

int pantalla_sort(Pantalla* list, int len, int order)
{
	int retorno = 0;

	if (list != NULL && len > 0 && order >0)
	{
		pantalla_sortByFileNumber(list, QTY_PANTALLAS, order);
		pantalla_sortByLastName(list, QTY_PANTALLAS, order);
		retorno = 0;
	}
	return retorno;
}

/**
 *  \brief pantalla_sortByLastName the elements in the array by last name and name
 * \param Employee* list: Pointer to array of employees
 * \param int len: Array length
 * \param int order: [2] indicate UP (order Z->A) - [1] indicate DOWN (order A->Z)
 * \return (-1) Error / (0) Ok
 */

int pantalla_sortByLastName(Pantalla* list, int len, int order)
{
	int retorno = -1;
	int sorted;
	Pantalla bufferPantalla;

	if(list != NULL && len > 0 && (order == 1 || order == 2))
	do
	{
		sorted = TRUE;
		for(int i = 0; i < (len-1); i++)
		{
			if((order == 1 &&
			  (strncmp(list[i].name, list[i + 1].name,LONG_NOMBRE)>0 ||
			  (strncmp(list[i].name, list[i + 1].name,LONG_NOMBRE)==0 &&
			   strncmp(list[i].address, list[i + 1].address,LONG_NOMBRE)>0))) ||
			   (order == 2 &&
			  (strncmp(list[i].name, list[i + 1].name,LONG_NOMBRE)<0 ||
			  (strncmp(list[i].name, list[i + 1].name,LONG_NOMBRE)==0 &&
			   strncmp(list[i].address, list[i + 1].address,LONG_NOMBRE)<0))))
			{
				bufferPantalla = list[i];
				list[i] = list[i + 1];
				list[i + 1] = bufferPantalla;
				sorted = FALSE;
			}
		}
	}while(sorted == FALSE);
	retorno = 0;
	return retorno;
}


