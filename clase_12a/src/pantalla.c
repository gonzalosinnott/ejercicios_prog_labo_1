/**
 *  \brief generateNewId: Generates a new ID that's +1 from previous loaded employee ID.
 */

static int generateNewId(void)
{
	static int id;

	id = id+1;
	return id;
}

/**
 *  \brief pantalla_addData: add in a existing list of employees the values received
 *  as parameters in the first empty position.
 * \param Employee* list: Pointer to array of employees
 * \param int len: Array length
 * \param int id: id generated by generateNewId() function
 * \param char name[]: Input by user from getEmployeeForm
 * \param char lastName[]: Input by user from getEmployeeForm
 * \param float salary: Input by user from getEmployeeForm
 * \param int sector: Input by user from getEmployeeForm
 * \return (-1) Error / (0) Ok
 */

int pantalla_addData(Pantalla* list,int len,int id,char name[], char address[], int pantallaType,float pricePerDay)
{
	int retorno = -1;
	int emptyIndex;

	if(checkFirstEmptyIndex(list, QTY_PANTALLAS, &emptyIndex)==0)
	{
		list[emptyIndex].id=id;
		strcpy(list[emptyIndex].name,name);
		strcpy(list[emptyIndex].address,address);
		list[emptyIndex].pantallaType = pantallaType;
		list[emptyIndex].pricePerDay = pricePerDay;
		list[emptyIndex].isEmpty=FALSE;
		printf("\nEl ID para la pantalla %s es: %d\n",list[emptyIndex].name,list[emptyIndex].id);
		retorno=0;
	}

    return retorno;
}

/**
 *  \brief pantalla_findById: find an Employee by Id then returns the index position in array.
 * \param Employee* list: Pointer to array of employees
 * \param int len: Array length
 * \param int id: id to search
 * \return Return employee index position or (-1) if ERROR
 */

int pantalla_findById(Pantalla* list, int len,int id)
{
	int retorno;

	if (list != NULL && len > 0 && id > 0)
	{
		for (int i = 0; i < len; i++)
		{
			if(list[i].isEmpty == FALSE && list[i].id == id )
			{
				retorno = i;
				break;
			}
		}
	}
	return retorno;;
}

