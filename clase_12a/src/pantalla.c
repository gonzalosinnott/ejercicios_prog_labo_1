/**
 * \brief pantalla_print: print the content of employees array
 * \param Employee* list: Pointer to array of employees
 * \param int len: Array length
 * \return (-1) Error / (0) Ok*
 */

int pantalla_print(Pantalla* list, int len)
{
	int retorno = -1;
	if(list != NULL && len > 0)
	{
		printf("\nID |Nombre        |Direccion          |Tipo  |Precio(Por Dia)\n");
		for(int i=0;i< len ;i++)
		{
			if(list[i].isEmpty == FALSE)
			{
				printf("%d %-15s %-15s %-6d  %-6.2f\n",list[i].id,list[i].name,list[i].address,list[i].pantallaType,list[i].pricePerDay);
			}
		}
		retorno = 0;
	}
	return retorno;
}
