/**
 * \brief pantalla_modify: Modifies the data of an Employee by given Id.
 * Allows to modify individual fields of the employee by a switch
 * \param Employee* list: Pointer to array of employees
 * \param int len: Array length
 * \param int firstLoad: variable to check if there is even one entry loaded
 * \return (-1) Error / (0) Ok
 */

int pantalla_modify(Pantalla* list, int len,int id)
{
	int retorno = -1;
	int choosenOption;
	char answer;
	int indexToModify;

	Pantalla bufferPantalla;

	indexToModify = pantalla_findById(list, QTY_PANTALLAS, id);

	if(list != NULL && len>0 && id > 0 && indexToModify > -1)
	{
		do
		{
			printf("Pantalla a modificar\n");
			printf("Nombre y Direccion: %s %s Tipo: %d Precio por dia %.2f.\n",
					list[indexToModify].name,list[indexToModify].address,list[indexToModify].pantallaType,list[indexToModify].pricePerDay);
			if(utn_getIntNumber("\nQue campo desea modificar:"
								"\n 1-Nombre."
								"\n 2-Direccion."
								"\n 3-Tipo:"
								"\n 4-Precio por Dia:"
								"\nOpcion:", "\nError.", &choosenOption, 3, 4, 1)==0)
		{
			switch(choosenOption)
			{
				case 1:
					if(utn_getString("\nNombre:","\nError. ",bufferPantalla.name,2,LONG_NOMBRE)==0)
					{
						strcpy(list[indexToModify].name,bufferPantalla.name);
					}
					break;
				case 2:
					if(utn_getAlphaNum("\nDireccion:","\nError. ",bufferPantalla.address,2,LONG_NOMBRE)==0)
					{
						strcpy(list[indexToModify].address,bufferPantalla.address);
					}
					break;
				case 3:
					if(utn_getIntNumber("Tipo (1-LED o 2-LCD):", "\nError. ", &bufferPantalla.pantallaType, 3, 2, 1)==0)
					{
						list[indexToModify].pantallaType=bufferPantalla.pantallaType;
					}
					break;
				case 4:
					if(utn_getFloatNumber("Precio por dia: ", "\nError. ", &bufferPantalla.pricePerDay, 3, INT_MAX, 0)==0)
					{
						list[indexToModify].pricePerDay=bufferPantalla.pricePerDay;
					}
					break;
			}
		}
		utn_getChar("¿Desea seguir modificando este ID?(Y/N)", "Error. ", &answer, 'Y', 'N', 3);
		}while(answer!='N');
		retorno = 0;
	}
	return retorno;
}

